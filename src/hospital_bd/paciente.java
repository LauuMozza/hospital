public class paciente {
    
    String IDCUENTAPAICNETE, IDREGISTRO, COSTOTOTAL;

    public paciente(String IDCUENTAPAICNETE, String IDREGISTRO, String COSTOTOTAL) {
        this.IDCUENTAPAICNETE = IDCUENTAPAICNETE;
        this.IDREGISTRO = IDREGISTRO;
        this.COSTOTOTAL = COSTOTOTAL;
    }

    public String getIDCUENTAPAICNETE() {
        return IDCUENTAPAICNETE;
    }

    public void setIDCUENTAPAICNETE(String IDCUENTAPAICNETE) {
        this.IDCUENTAPAICNETE = IDCUENTAPAICNETE;
    }

    public String getIDREGISTRO() {
        return IDREGISTRO;
    }

    public void setIDREGISTRO(String IDREGISTRO) {
        this.IDREGISTRO = IDREGISTRO;
    }

    public String getCOSTOTOTAL() {
        return COSTOTOTAL;
    }

    public void setCOSTOTOTAL(String COSTOTOTAL) {
        this.COSTOTOTAL = COSTOTOTAL;
    }
    
}
